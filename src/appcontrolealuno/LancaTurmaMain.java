    private void jTxtCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTxtCodigoActionPerformed
        // Busca item e coloca descricao e pula para quantidade:

        try {
            CaixaAuxTO dataCaixa = ParametroDAO.buscaDataCaixa();
            jTxtDataVenda.setText(dataCaixa.getDataCx());
        } catch (DaoException | SQLException ex) {
            Logger.getLogger(LancaTurmaMain.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage() + " \nErro Acesso ao Banco de Dados");
        }

        ItemTO it = new ItemTO();
        // Alinhas colunas a direita
        DefaultTableCellRenderer alignDireita = new DefaultTableCellRenderer();
        alignDireita.setHorizontalAlignment(JLabel.RIGHT);
        TableColumn co04 = jTableMovCx.getColumnModel().getColumn(4);
        co04.setCellRenderer(alignDireita);
        TableColumn co03 = jTableMovCx.getColumnModel().getColumn(3);
        co03.setCellRenderer(alignDireita);
        TableColumn co02 = jTableMovCx.getColumnModel().getColumn(2);
        co02.setCellRenderer(alignDireita);

        DefaultTableCellRenderer alignCentro = new DefaultTableCellRenderer();
        alignCentro.setHorizontalAlignment(JLabel.CENTER);
        TableColumn co00 = jTableMovCx.getColumnModel().getColumn(0);
        co00.setCellRenderer(alignCentro);

        try {
            if (jTxtCodigo.getText().toUpperCase().substring(0, 1).equals("R")) {
                it = ItemDAO.detalharItemReferencia(jTxtCodigo.getText().substring(1, jTxtCodigo.getText().length()));
            } else {
                if (jTxtCodigo.getText().length() > 5) {
                    if (jTxtCodigo.getText().substring(0, 1).equals("2") & parametroGeral.getEtiquetaComidaKg().equals("S")) {
                        it = ItemDAO.detalharItemETQ(jTxtCodigo.getText());
                    } else {
                        it = ItemDAO.detalharItemEAN(jTxtCodigo.getText());
                    }
                } else {
                    it = ItemDAO.detalharItem(jTxtCodigo.getText());
                }
            }
        } catch (DaoException | SQLException ex) {
            Logger.getLogger(LancaTurmaMain.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage() + " \nErro na aplicacao");
            //
            SwingUtilities.invokeLater(new Runnable() {
                @Override
                public void run() {
                    jTxtCodigo.setBackground(new Color(255, 0, 102));
                    jTxtCodigo.requestFocus();
                }
            });
        }
        if (it == null) {
            JOptionPane.showMessageDialog(this, "Codigo invÃ¡lido!");
            SwingUtilities.invokeLater(new Runnable() {
                @Override
                public void run() {
                    jTxtCodigo.setBackground(new Color(255, 0, 102));
                    jTxtCodigo.requestFocus();
                }
            });
        } else {
            jTxtCodigo.setText(it.getCodigo());
            jTxtDescricao.setText(geral.Geral.CortaString(it.getDescricao(), 30));
            jTxtSaldo.setText(duasDecimais.format(it.getEstoque()));
        }
        jTxtCodigo.setBackground(new Color(255, 0, 102));
        jTxtQtd.requestFocus();
    }//GEN-LAST:event_jTxtCodigoActionPerformed

