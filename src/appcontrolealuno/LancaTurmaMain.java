    private void inicioFechamentoEntrada() throws NumberFormatException, HeadlessException {
        // Pede confirmaÃ§Ã£o para emissao do relatÃ³rio
        Object[] options = {"Sim", "NÃ£o"};
        int q = JOptionPane.showOptionDialog(null,
                "Confirma entrada ?", "SaÃ­da",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                options, options[1]);
        if (q == JOptionPane.YES_OPTION) {

            // Venda = 0
            if (jTxtTotalGeral.getText().equals("0.00")) {
                JOptionPane.showMessageDialog(this, "Entrada sem Item!");
                return;
            }
            // Monta array objetos para gravar
            // Declarar Movimento TO
            int codigoFornecedor;
            if (!jTxtSerie.getText().equals("")) {
                codigoFornecedor = Integer.valueOf(jTxtCodigoTurma.getText());
            } else {
                codigoFornecedor = 0;
            }

            ArrayList<MovimentoTO> m = new ArrayList<>();
            for (int i = 0; i < Integer.parseInt(jTxtNumItens.getText()); i++) {
                MovimentoTO to = new MovimentoTO();
                to.setNdoc(Integer.parseInt(jTxtAlunos.getText()));
                to.setSequencia(i + 1);
                to.setCodigo(jTableMovCx.getValueAt(i, 0).toString());
                to.setTipoMov("E");
                to.setValorUnit(Double.parseDouble(jTableMovCx.getValueAt(i, 2).toString()));
                to.setQuant(Double.parseDouble(jTableMovCx.getValueAt(i, 3).toString().replace(",", ".")));
                to.setValorTotal(Double.parseDouble(jTableMovCx.getValueAt(i, 4).toString()));
                to.setDescricao(jTableMovCx.getValueAt(i, 1).toString());
                to.setDataMov(dataHojeS);
                to.setCodigoFornecedor(codigoFornecedor);
                m.add(to);
            }
            ArrayList<RelEntradaTO> relEntrada;
            try {
                //Grava dados no banco
                MovimentacaoDAO.inserirMovimentoEntrada(m);
                relEntrada = MovimentacaoDAO.transformaMovimentoTOemRelEntradaTO(m, "Entrada Manual.");
            } catch (DaoException | SQLException ex) {
                Logger.getLogger(LancaTurmaMain.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Erro ao gravar entrada");
                return;
            }
            if (relEntrada.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Iten(s) nÃ£o encontrado(s)!");
                return;
            }
            // limpa campos da tela
            getTableModel().setRowCount(0);
            jTxtTotalGeral.setText("0.00");
            jTxtAlunos.setText("");
            jTxtNumItens.setText("");
            // Imprime tiket venda
            try {
                // Pergunta se imprime ou gera PDF
                Object[] options1 = {"Impressora", "Tela"};
                int q1 = JOptionPane.showOptionDialog(null,
                        "O relatÃ³rio serÃ¡?", "SaÃ­da",
                        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                        options1, options1[1]);
                if (q1 == JOptionPane.YES_OPTION) {

                    // compilacao do JRXML
                    JasperReport report = JasperCompileManager.compileReport(geral.Geral.getDiretorioAtual() + "\\Relatorios\\TiketEntrada.jrxml");
                    JasperPrint print = JasperFillManager.fillReport(report, null, new JRBeanCollectionDataSource(relEntrada));
                        // exportacao do relatorio para outro formato, no caso PDF 
                    //JasperExportManager.exportReportToPdfFile(print, geral.Geral.getDiretorioAtual() + "\\Relatorios\\RelatorioClientes.pdf");  
                    //JasperViewer.viewReport(print, false);
                    JasperPrintManager.printPage(print, 0, false);

                } else {
                    JasperReport report = JasperCompileManager.compileReport(geral.Geral.getDiretorioAtual() + "\\Relatorios\\TiketEntrada.jrxml");
                    JasperPrint print = JasperFillManager.fillReport(report, null, new JRBeanCollectionDataSource(relEntrada));
                    // exportacao do relatorio para outro formato, no caso PDF 
                    JasperExportManager.exportReportToPdfFile(print, geral.Geral.getDiretorioAtual() + "\\Relatorios\\TiketEntrada.pdf");
                    JasperViewer.viewReport(print, false);
                }
            } catch (JRException | ValidacaoException ex) {
                Logger.getLogger(LancaTurmaMain.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Erro ao imprimir entrada!");
            }
        }

    }


